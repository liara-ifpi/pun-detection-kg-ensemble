def normalize_stopwords(words: List[str], strip_accents: Optional[str]) -> List[str]:
    out = []
    for w in words:
        w = str(w).lower()
        if strip_accents == "unicode":
            w = strip_accents_unicode(w)
        out.append(w)
    return sorted(set(out))


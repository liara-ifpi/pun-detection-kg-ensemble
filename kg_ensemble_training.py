GRAPH_EMB_DIM = 4
GRAPH_EMB_EXTRA = 2
GRAPH_FEATURE_DIM = GRAPH_EMB_DIM + GRAPH_EMB_EXTRA

TOKEN_RE = re.compile(r"[A-Za-zÀ-ÖØ-öø-ÿ]+", re.UNICODE)

def tokenize_for_graph(text: str) -> List[str]:
    return [t.lower() for t in TOKEN_RE.findall(text)]

def read_gpickle_robust(path: Path):
    try:
        return nx.read_gpickle(path)
    except Exception:
        with path.open("rb") as f:
            return pickle.load(f)

def nx_to_sparse_adjacency(G: nx.Graph, nodelist: List[str], weight: str = "weight") -> csr_matrix:
    if hasattr(nx, "to_scipy_sparse_array"):
        A = nx.to_scipy_sparse_array(G, nodelist=nodelist, weight=weight, dtype=float, format="csr")
        return csr_matrix(A)
    return nx.to_scipy_sparse_matrix(G, nodelist=nodelist, weight=weight, dtype=float, format="csr")

def build_node_embeddings_svd(
    G: nx.Graph, dim: int, seed: int = 40
) -> Tuple[List[str], Dict[str, int], np.ndarray]:
    """
    Cria embedding por nó com TruncatedSVD na matriz de adjacência ponderada.
    Retorna:
      - nodelist
      - node_to_idx
      - node_emb (n_nodes x dim)
    """
    nodelist = list(G.nodes())
    node_to_idx = {n: i for i, n in enumerate(nodelist)}

    A = nx_to_sparse_adjacency(G, nodelist=nodelist, weight="weight")
    svd = TruncatedSVD(n_components=dim, random_state=seed)
    node_emb = svd.fit_transform(A).astype(np.float32)  # (n_nodes, dim)

    return nodelist, node_to_idx, node_emb

def doc_features_from_graph(texts: pd.Series, node_to_idx: Dict[str, int], node_emb: np.ndarray) -> np.ndarray:
    """
    Para cada texto: média dos embeddings dos nós correspondentes aos tokens que aparecem no grafo
    + cobertura (hits/total_tokens)
    + log1p(hits)
    """
    n = len(texts)
    feats = np.zeros((n, GRAPH_FEATURE_DIM), dtype=np.float32)

    for i, txt in enumerate(texts.astype(str).tolist()):
        toks = tokenize_for_graph(txt)
        if not toks:
            continue

        idxs = [node_to_idx[t] for t in toks if t in node_to_idx]
        hits = len(idxs)
        total = len(toks)

        if hits > 0:
            feats[i, :GRAPH_EMB_DIM] = node_emb[idxs].mean(axis=0)

        coverage = hits / total if total > 0 else 0.0
        feats[i, GRAPH_EMB_DIM] = coverage
        feats[i, GRAPH_EMB_DIM + 1] = np.log1p(hits)

    return feats


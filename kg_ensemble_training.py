def resolve_path(preferred_path: str, filename_fallback: Optional[str] = None) -> Path:
    """
    Tenta achar o arquivo:
    1) no caminho preferido (ex: corpus/train.jsonl)
    2) relativo ao cwd
    3) em ./, ./corpus, ./outputs pelo nome fallback
    4) busca recursiva (primeiro match)
    """
    preferred = Path(preferred_path)
    candidates = []

    candidates.append(preferred)
    candidates.append(Path.cwd() / preferred)

    if filename_fallback:
        candidates.append(Path.cwd() / filename_fallback)
        candidates.append(Path.cwd() / "corpus" / filename_fallback)
        candidates.append(Path.cwd() / "outputs" / filename_fallback)

    for c in candidates:
        if c.exists() and c.is_file():
            return c.resolve()

    name_to_find = filename_fallback or preferred.name
    matches = [m for m in Path.cwd().rglob(name_to_find) if m.is_file()]

    if matches:
        return matches[0].resolve()

    raise FileNotFoundError(
        f"Não encontrei '{preferred_path}'. Também tentei '{name_to_find}' em ./, ./corpus, ./outputs e busca recursiva.\n"
        f"Diretório atual: {Path.cwd()}"
    )

def load_jsonl(path: Path) -> pd.DataFrame:
    rows = []
    with path.open("r", encoding="utf-8") as f:
        for i, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except json.JSONDecodeError as e:
                raise ValueError(f"JSON inválido em {path} na linha {i}: {e}") from e

    if not rows:
        raise ValueError(f"O arquivo {path} está vazio (ou só com linhas em branco).")

    return pd.DataFrame(rows)

def create_x_y(df: pd.DataFrame) -> Tuple[pd.Series, pd.Series]:
    if "text" not in df.columns or "label" not in df.columns:
        raise ValueError(f"Esperado colunas 'text' e 'label', mas encontrei: {list(df.columns)}")
    x = df["text"].astype(str)
    y = df["label"]
    if x.isna().any():
        raise ValueError("Há valores nulos em 'text'.")
    if y.isna().any():
        raise ValueError("Há valores nulos em 'label'.")
    return x, y


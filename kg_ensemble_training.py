def make_vectorizer() -> TfidfVectorizer:
    return TfidfVectorizer(
        ngram_range=(1, 2),
        stop_words=stop_words_vec,
        lowercase=True,
        strip_accents=STRIP_ACCENTS,
    )

def make_ensemble() -> VotingClassifier:
    rf = RandomForestClassifier(
        n_estimators=100, criterion='entropy', max_depth=15, random_state=40
    )

    lr = LogisticRegression(
        random_state=SEED,
        max_iter=100,
        class_weight="balanced"
    )

    svm_base = LinearSVC(
        random_state=SEED,
        class_weight="balanced"
    )

    svm = CalibratedClassifierCV(
        svm_base,
        method="sigmoid",
        cv=3
    )

    voting = VotingClassifier(
        estimators=[("rf", rf), ("lr", lr), ("svm", svm)],
        voting="soft",
        n_jobs=30
    )
    return voting

def evaluate(name: str, y_true, y_pred, labels=None):
    acc = accuracy_score(y_true, y_pred)
    f1m = f1_score(y_true, y_pred, average="macro")
    print(f"\n=== {name} ===")
    print("Accuracy:", acc)
    print("Macro-F1:", f1m)
    print(classification_report(y_true, y_pred))

    ConfusionMatrixDisplay.from_predictions(
        y_true, y_pred,
        labels=labels,
        cmap=None,
        values_format="d"
    )
    plt.title(f"Matriz de Confusão — {name}")
    plt.show()

    return acc, f1m

def get_graph_block(graph_name: str, split: str) -> np.ndarray:
    return graph_doc_feats[graph_name][split]

def stack_graph_features(blocks: List[np.ndarray]) -> np.ndarray:
    """
    Empilha features *do mesmo split* lado a lado (ex.: Cooc + PPMI + PunContext).
    NÃO mistura train/val/test.
    """
    if not blocks:
        raise ValueError("blocks vazio.")
    return np.hstack(blocks).astype(np.float32)

def fuse_text_and_graph(X_text_csr, Xg_scaled: np.ndarray):
    """Une TF-IDF (sparse) com grafo (dense->csr)."""
    return hstack([X_text_csr, csr_matrix(Xg_scaled)], format="csr")

def run_test_experiment(exp_name: str, graph_names: List[str]):
    """
    Treina APENAS no TRAIN e avalia no TEST.
    (Proibido usar/concatenar TRAIN+VAL em qualquer etapa.)
    """
    vec = make_vectorizer()
    Xtr_text = vec.fit_transform(x_train)
    Xte_text = vec.transform(x_test)

    if not graph_names:
        model = make_ensemble()
        model.fit(Xtr_text, y_train)
        pred = model.predict(Xte_text)
        acc, f1m = evaluate(f"TEST — {exp_name}", y_test, pred, labels=sorted(pd.unique(y_train)))
        return {"setup": exp_name, "test_acc": acc, "test_f1_macro": f1m}

    Xtr_g_raw = stack_graph_features([get_graph_block(g, "train") for g in graph_names])
    Xte_g_raw = stack_graph_features([get_graph_block(g, "test")  for g in graph_names])

    scaler = StandardScaler()
    scaler.fit(Xtr_g_raw)

    Xtr_g = scaler.transform(Xtr_g_raw).astype(np.float32)
    Xte_g = scaler.transform(Xte_g_raw).astype(np.float32)

    Xtr = fuse_text_and_graph(Xtr_text, Xtr_g)
    Xte = fuse_text_and_graph(Xte_text, Xte_g)

    model = make_ensemble()
    model.fit(Xtr, y_train)
    pred = model.predict(Xte)

    acc, f1m = evaluate(f"TEST — {exp_name}", y_test, pred, labels=sorted(pd.unique(y_train)))
    return {"setup": exp_name, "test_acc": acc, "test_f1_macro": f1m}

